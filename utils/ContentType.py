def content_type(data):

    if data == "txt":
        return "text/plain"
    elif data == "html":
        return "text/html"
    elif data == "css":
        return "text/css"
    elif data == "pdf":
        return "application/pdf"
    elif data == "jpg":
        return "image/jpeg"
    elif data == "png":
        return "image/png"
